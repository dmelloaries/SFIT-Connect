import tkinter as tk
from tkinter import messagebox

class ScoreCalculator:
    def __init__(self, root):
        self.root = root
        self.root.title("Score Calculator")
        
        self.label1 = tk.Label(root, text="Enter Score 1:")
        self.label1.pack()
        self.score1_entry = tk.Entry(root)
        self.score1_entry.pack()

        self.label2 = tk.Label(root, text="Enter Score 2:")
        self.label2.pack()
        self.score2_entry = tk.Entry(root)
        self.score2_entry.pack()

        self.calculate_button = tk.Button(root, text="Calculate", command=self.calculate_score)
        self.calculate_button.pack()

        # Additional Features
        self.label3 = tk.Label(root, text="Enter Score 3:")
        self.label3.pack()
        self.score3_entry = tk.Entry(root)
        self.score3_entry.pack()

        self.label4 = tk.Label(root, text="Enter Score 4:")
        self.label4.pack()
        self.score4_entry = tk.Entry(root)
        self.score4_entry.pack()

        self.clear_button = tk.Button(root, text="Clear", command=self.clear_entries)
        self.clear_button.pack()

        self.average_button = tk.Button(root, text="Calculate Average", command=self.calculate_average)
        self.average_button.pack()

        self.max_button = tk.Button(root, text="Find Max Score", command=self.find_max_score)
        self.max_button.pack()

        self.min_button = tk.Button(root, text="Find Min Score", command=self.find_min_score)
        self.min_button.pack()

        self.exit_button = tk.Button(root, text="Exit", command=root.quit)
        self.exit_button.pack()

    def calculate_score(self):
        try:
            score1 = float(self.score1_entry.get())
            score2 = float(self.score2_entry.get())
            total_score = score1 + score2
            messagebox.showinfo("Total Score", f"Total Score: {total_score}")
        except ValueError:
            messagebox.showerror("Error", "Please enter valid scores.")

    # Additional Features
    def clear_entries(self):
        self.score1_entry.delete(0, tk.END)
        self.score2_entry.delete(0, tk.END)
        self.score3_entry.delete(0, tk.END)
        self.score4_entry.delete(0, tk.END)

    def calculate_average(self):
        try:
            score1 = float(self.score1_entry.get())
            score2 = float(self.score2_entry.get())
            score3 = float(self.score3_entry.get())
            score4 = float(self.score4_entry.get())
            average_score = (score1 + score2 + score3 + score4) / 4
            messagebox.showinfo("Average Score", f"Average Score: {average_score}")
        except ValueError:
            messagebox.showerror("Error", "Please enter valid scores.")

    def find_max_score(self):
        try:
            scores = [float(self.score1_entry.get()), float(self.score2_entry.get()), 
                      float(self.score3_entry.get()), float(self.score4_entry.get())]
            max_score = max(scores)
            messagebox.showinfo("Max Score", f"Max Score: {max_score}")
        except ValueError:
            messagebox.showerror("Error", "Please enter valid scores.")

    def find_min_score(self):
        try:
            scores = [float(self.score1_entry.get()), float(self.score2_entry.get()), 
                      float(self.score3_entry.get()), float(self.score4_entry.get())]
            min_score = min(scores)
            messagebox.showinfo("Min Score", f"Min Score: {min_score}")
        except ValueError:
            messagebox.showerror("Error", "Please enter valid scores.")

