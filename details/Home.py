import tkinter as tk
import random

# Function to generate random gibberish
def generate_gibberish():
    gibberish = ''.join(random.choices('abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789', k=random.randint(5, 20)))
    return gibberish

# Function to update the label with random gibberish
def update_label():
    gibberish_text.set(generate_gibberish())

# Function to change the background color
def change_bg_color():
    colors = ['#FF5733', '#33FF57', '#5733FF', '#FF33F3', '#F3FF33']
    bg_color = random.choice(colors)
    root.configure(background=bg_color)

# Creating the main window
root = tk.Tk()
root.title("Python Homepage")
root.geometry("500x400")

# Creating a frame to hold the content
frame = tk.Frame(root)
frame.pack(pady=20)

# Creating a label to display random gibberish
gibberish_text = tk.StringVar()
gibberish_label = tk.Label(frame, textvariable=gibberish_text, font=("Arial", 16), wraplength=450)
gibberish_label.pack(pady=10)

# Creating a button to generate new random gibberish
generate_button = tk.Button(frame, text="Generate Gibberish", command=update_label)
generate_button.pack()

# Creating a button to change background color
color_button = tk.Button(frame, text="Change Background Color", command=change_bg_color)
color_button.pack()

# Initial generation of gibberish
update_label()

# Running the Tkinter event loop
root.mainloop()

